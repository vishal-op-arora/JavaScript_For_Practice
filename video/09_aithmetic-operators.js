/**
 * Airthmatic Operators
 *  + : Addition
 *  - : Subtraction
 *  / : Division
 *  * : Multiplication
 *  % : Remainder
 * */ 


var x = 12, y = 11; 

console.log( x  + y);
console.log( x  - y);
console.log( x  / y);
console.log( x  * y);
console.log( x  % y);

/**
 * + operator is overloaded 
 *  - Add two numbers
 *  - Concatinate two strings
 */
console.log("Hello," + ' ' + "JavaScript World!!")


